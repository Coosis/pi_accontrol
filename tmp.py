import threading
import sys

def send_ir_signal(timings):
    pi = pigpio.pi()
    if not pi.connected:
        print("Failed to connect to pigpio daemon")

    try:
        for i, duration in enumerate(timings):
            mode = CARRIER_FREQ if i % 2 == 0 else 0
            pi.hardware_PWM(PWM_PIN, mode, DUTY_CYCLE if mode else 0)
            # time.sleep(duration / 1_000_000)
            time.sleep((duration-125) / 1_000_000)
    except Exception as e:
        print(f"Error during IR signal transmission: {e}")
    finally:
        time.sleep(1)  # Ensure the signal is fully sent
        pi.hardware_PWM(PWM_PIN, 0, 0)  # Ensure PWM is turned off
        pi.stop()  # Proper cleanup
        print("Signal sent and resources cleaned up")

    print("Signal sent!")

def async_send_ir_signal(timings):
    thread = threading.Thread(target=send_ir_signal, args=(timings,))
    thread.start()

